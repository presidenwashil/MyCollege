package contoharray3;

public class ContohArray3 {

    public static void main(String[] args) {
        String[] kota = {"Samarinda", "Balikpapan", "Tenggarong",
            "Bontang", "Sangatta"};
        //Menampilkan elemen array
        for (int i = 0; i < kota.length; i++) {
            System.out.println(kota[i]);
        }
    }
}
