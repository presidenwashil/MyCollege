package cekstring;

public class CekString {

    public static void main(String[] args) {
        String kar1 = "saya";
        //  Mengecek apakah isi String kar1 diakhiri dengan huruf s
        System.out.println("Apakah kata '" + kar1 + "' diakhiri dengan huruf 's'? : " + kar1.endsWith("s") + "\n");
    }
}
